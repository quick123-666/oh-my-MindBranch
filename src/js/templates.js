  // children 数组格式：[text, [child1, child2, ...]]
  function buildTemplate(rootText, children) {
    const rootId = 'tpl_root';
    const nodes = [{
      id: rootId,
      text: rootText,
      parentId: null,
      children: [],
      collapsed: false,
      x: 0, y: 0
    }];
    let idCounter = 0;
    const genId = () => `tpl_${++idCounter}`;

    function walk(parentId, items) {
      const parentNode = nodes.find(n => n.id === parentId);
      items.forEach(item => {
        const [text, subChildren] = item;
        const id = genId();
        nodes.push({
          id,
          text,
          parentId,
          children: [],
          collapsed: false,
          x: 0, y: 0
        });
        parentNode.children.push(id);
        if (subChildren && subChildren.length > 0) {
          walk(id, subChildren);
        }
      });
    }

    walk(rootId, children);
    return {
      title: rootText,
      rootId,
      nodes
    };
  }

